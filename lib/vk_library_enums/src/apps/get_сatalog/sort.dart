part of vk_library_enums;

/// A way of sorting apps of the [Apps.getCatalog] method.
enum SortAppsGetCatalog {
  /// Popular of the day.
  POPULAR_TODAY,

  /// By attendance.
  VISITORS,

  /// By the date the application was created.
  CREATE_DATE,

  /// According to the growth rate.
  GROWTH_RATE,

  /// Popular for the week.
  POPULAR_WEEK,
}
